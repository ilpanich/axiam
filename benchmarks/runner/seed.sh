#!/usr/bin/env bash
# seed.sh — provision a benchmark fixture (org/tenant/realm, a user, and a
# confidential client able to do client_credentials + refresh) for one target,
# then write a seed env file the runner sources:
#
#   results/<target>.seed.env   (BENCH_ORG_ID, BENCH_TENANT_ID, BENCH_CLIENT_ID,
#                                BENCH_CLIENT_SECRET, BENCH_USERNAME, BENCH_PASSWORD)
#
# Usage: seed.sh <target> [base_url]
set -euo pipefail

TARGET="${1:?target (axiam|keycloak|zitadel)}"
HERE="$(cd "$(dirname "$0")" && pwd)"
RESULTS="${BENCH_RESULTS_DIR:-$HERE/../results}"
mkdir -p "$RESULTS"
SEED_ENV="$RESULTS/${TARGET}.seed.env"

BENCH_USERNAME="${BENCH_USERNAME:-benchuser}"
BENCH_PASSWORD="${BENCH_PASSWORD:-Bench@User123!}"

# Seeding always talks plaintext to the app port; TLS is a transport concern
# exercised during the measured run, not during provisioning.
BASE="${2:-http://localhost:${BENCH_APP_PORT:-8090}}"

write_seed() {
  cat > "$SEED_ENV" <<EOF
# Generated by seed.sh for target=$TARGET
export BENCH_TARGET=$TARGET
export BENCH_ORG_ID=${ORG_ID:-}
export BENCH_ORG_SLUG=${ORG_SLUG:-bench-org}
export BENCH_TENANT_ID=${TENANT_ID:-}
export BENCH_TENANT_SLUG=${TENANT_SLUG:-default}
export BENCH_REALM=${REALM:-bench}
export BENCH_USERNAME=$BENCH_USERNAME
export BENCH_PASSWORD='$BENCH_PASSWORD'
export BENCH_CLIENT_ID=${CLIENT_ID:-bench-client}
export BENCH_CLIENT_SECRET='${CLIENT_SECRET:-}'
export BENCH_SUBJECT_ID=${BENCH_SUBJECT_ID:-}
export BENCH_RESOURCE_ID=${BENCH_RESOURCE_ID:-}
# Zitadel-only: token audience (project id) + a separate resource-server (API
# app) client used for token introspection. Empty for axiam/keycloak.
export BENCH_PROJECT_ID=${PROJECT_ID:-}
export BENCH_INTROSPECT_CLIENT_ID=${INTROSPECT_CLIENT_ID:-}
export BENCH_INTROSPECT_CLIENT_SECRET='${INTROSPECT_CLIENT_SECRET:-}'
EOF
  echo "[seed] wrote $SEED_ENV"
}

# ---------------------------------------------------------------------------
# AXIAM seeding notes (post-1.0.0-alpha):
#   * Bootstrap (POST /api/v1/admin/bootstrap) now creates the org + default
#     tenant + admin itself — it no longer accepts pre-made org_id/tenant_id.
#     It is fail-closed: the bench compose sets AXIAM_BOOTSTRAP_ADMIN_EMAIL to
#     admin@bench.dev so the gate is satisfied without a setup_token, and it is
#     one-shot (a second call returns 409, which we handle).
#   * Login delivers tokens ONLY via Set-Cookie (axiam_access/axiam_csrf/…), so
#     we drive the admin session through a curl cookie jar rather than a Bearer
#     header, and every non-GET under /api/v1 must echo the axiam_csrf cookie in
#     an X-CSRF-Token header (double-submit).
#   * The gRPC/REST authz scenarios need a real (allowed=true) decision, so we
#     seed a resource + a role holding a "read" permission grant, assigned to the
#     bench user scoped to that resource, and export BENCH_RESOURCE_ID /
#     BENCH_SUBJECT_ID for the scenarios and SDK benches.
seed_axiam() {
  command -v jq >/dev/null || { echo "[seed/axiam] jq is required (see README prerequisites)"; exit 1; }
  JAR="$(mktemp)"; trap 'rm -f "$JAR"' RETURN
  local ADMIN_EMAIL="${BENCH_ADMIN_EMAIL:-admin@bench.dev}"
  local ADMIN_PW="${BENCH_ADMIN_PASSWORD:-Bench@Admin123!}"
  # Login requires org context (a tenant slug is only unique within an org), so we
  # pin a deterministic org slug and pass it explicitly — this also survives a
  # re-seed (bootstrap 409) where we can't read it back from the response.
  TENANT_SLUG=default
  ORG_SLUG=bench-org

  # Authenticated JSON call against /api/v1 with the admin cookie jar + CSRF
  # double-submit. Usage: api METHOD PATH [JSON-BODY].
  api() {
    local method="$1" path="$2" data="${3:-}" csrf
    csrf="$(awk -F'\t' '$6=="axiam_csrf"{v=$7} END{print v}' "$JAR")"
    if [ -n "$data" ]; then
      curl -sSk -b "$JAR" -c "$JAR" -X "$method" "$BASE$path" \
        -H "Content-Type: application/json" -H "X-CSRF-Token: $csrf" -d "$data"
    else
      curl -sSk -b "$JAR" -c "$JAR" -X "$method" "$BASE$path" -H "X-CSRF-Token: $csrf"
    fi
  }
  # Create an entity, or find the existing one on re-seed. `.. | objects` walks
  # any response wrapper (bare array, {data:[…]}, {items:[…]}) to find the row
  # whose FIELD == VALUE and return its id.
  create_or_find() {  # PATH CREATE_JSON MATCH_FIELD MATCH_VALUE
    local path="$1" cjson="$2" field="$3" val="$4" id
    id=$(api POST "$path" "$cjson" | jq -r '.id // empty' 2>/dev/null || true)
    if [ -z "$id" ]; then
      id=$(api GET "$path" | jq -r --arg f "$field" --arg v "$val" \
        '[.. | objects | select(.[$f]==$v) | .id] | first // empty' 2>/dev/null || true)
    fi
    echo "$id"
  }

  echo "[seed/axiam] bootstrapping org + default tenant + admin (gated by AXIAM_BOOTSTRAP_ADMIN_EMAIL)"
  local boot code body
  boot=$(curl -sSk -o - -w $'\n%{http_code}' -c "$JAR" -X POST "$BASE/api/v1/admin/bootstrap" \
    -H "Content-Type: application/json" \
    -d "{\"organization_name\":\"Bench Org\",\"organization_slug\":\"$ORG_SLUG\",\"tenant_name\":\"Bench Tenant\",\"tenant_slug\":\"$TENANT_SLUG\",\"email\":\"$ADMIN_EMAIL\",\"username\":\"admin\",\"password\":\"$ADMIN_PW\"}")
  code="${boot##*$'\n'}"; body="${boot%$'\n'*}"
  case "$code" in
    201)
      ORG_ID=$(echo "$body" | jq -r '.organization_id // empty')
      ORG_SLUG=$(echo "$body" | jq -r '.organization_slug // "bench-org"')
      TENANT_ID=$(echo "$body" | jq -r '.tenant_id // empty')
      TENANT_SLUG=$(echo "$body" | jq -r '.tenant_slug // "default"')
      ;;
    409) echo "[seed/axiam] already bootstrapped — recovering tenant via admin login" ;;
    *)
      echo "[seed/axiam] bootstrap failed (HTTP $code): $body"
      echo "  hint: the server must have AXIAM_BOOTSTRAP_ADMIN_EMAIL=$ADMIN_EMAIL set"
      echo "        (the bench compose does this) OR you must pass a valid setup_token."
      exit 1 ;;
  esac

  echo "[seed/axiam] logging in as admin (cookie session)"
  local login_body
  login_body=$(curl -sSk -c "$JAR" -X POST "$BASE/api/v1/auth/login" -H "Content-Type: application/json" \
    -d "{\"org_slug\":\"$ORG_SLUG\",\"tenant_slug\":\"$TENANT_SLUG\",\"username_or_email\":\"admin\",\"password\":\"$ADMIN_PW\"}")
  if ! awk -F'\t' '$6=="axiam_access"{f=1} END{exit !f}' "$JAR"; then
    echo "[seed/axiam] admin login failed (no session cookie): $login_body"; exit 1
  fi
  # Recover ids we may not have on a re-seed (bootstrap 409).
  [ -z "${TENANT_ID:-}" ] && TENANT_ID=$(echo "$login_body" | jq -r '.user.tenant_id // empty')
  [ -z "${TENANT_ID:-}" ] && TENANT_ID=$(api GET /api/v1/auth/me | jq -r '.tenant_id // empty')
  [ -z "${TENANT_ID:-}" ] && { echo "[seed/axiam] could not determine tenant_id"; exit 1; }
  [ -z "${ORG_ID:-}" ] && ORG_ID=$(api GET /api/v1/auth/me | jq -r '.org_id // .organization_id // empty')

  echo "[seed/axiam] creating benchmark user"
  USER_ID=$(create_or_find /api/v1/users \
    "{\"username\":\"$BENCH_USERNAME\",\"email\":\"bench@bench.dev\",\"password\":\"$BENCH_PASSWORD\"}" \
    username "$BENCH_USERNAME")
  [ -z "$USER_ID" ] && echo "[seed/axiam] WARN: could not create/find bench user"

  echo "[seed/axiam] creating benchmark resource"
  RESOURCE_ID=$(create_or_find /api/v1/resources \
    '{"name":"bench-resource","resource_type":"bench"}' name "bench-resource")
  [ -z "$RESOURCE_ID" ] && echo "[seed/axiam] WARN: could not create/find bench resource"

  echo "[seed/axiam] creating role + read permission"
  ROLE_ID=$(create_or_find /api/v1/roles \
    '{"name":"bench-reader","description":"Bench read role","is_global":false}' name "bench-reader")
  PERM_ID=$(create_or_find /api/v1/permissions \
    '{"action":"read","description":"Bench read permission"}' action "read")

  if [ -n "$ROLE_ID" ] && [ -n "$PERM_ID" ]; then
    echo "[seed/axiam] granting read permission to role (idempotent)"
    api POST "/api/v1/roles/$ROLE_ID/permissions" "{\"permission_id\":\"$PERM_ID\"}" >/dev/null 2>&1 || true
  fi
  if [ -n "$ROLE_ID" ] && [ -n "$USER_ID" ] && [ -n "$RESOURCE_ID" ]; then
    echo "[seed/axiam] assigning role to bench user (scoped to resource)"
    api POST "/api/v1/roles/$ROLE_ID/users" \
      "{\"user_id\":\"$USER_ID\",\"resource_id\":\"$RESOURCE_ID\"}" >/dev/null 2>&1 || true
  fi

  echo "[seed/axiam] creating confidential oauth2 client (client_credentials+refresh)"
  local CLIENT_RESP
  CLIENT_RESP=$(api POST /api/v1/oauth2-clients \
    '{"name":"bench-client","redirect_uris":["http://localhost/cb"],"grant_types":["client_credentials","refresh_token","authorization_code"],"scopes":["openid"]}')
  CLIENT_ID=$(echo "$CLIENT_RESP" | jq -r '.client_id // empty')
  CLIENT_SECRET=$(echo "$CLIENT_RESP" | jq -r '.client_secret // empty')
  [ -z "$CLIENT_ID" ] && echo "[seed/axiam] WARN: client creation response: $CLIENT_RESP"

  # Export the authz fixtures the scenarios/SDK benches need.
  BENCH_SUBJECT_ID="$USER_ID"
  BENCH_RESOURCE_ID="$RESOURCE_ID"
  write_seed
}

# ---------------------------------------------------------------------------
seed_keycloak() {
  REALM="${REALM:-bench}"
  local ADMIN="${KC_ADMIN:-admin}" PW="${KC_ADMIN_PASSWORD:-admin}"
  echo "[seed/keycloak] obtaining admin token"
  local AT
  AT=$(curl -sf -X POST "$BASE/realms/master/protocol/openid-connect/token" \
    -d "grant_type=password&client_id=admin-cli&username=$ADMIN&password=$PW" \
    | sed -n 's/.*"access_token":"\([^"]*\)".*/\1/p')
  [ -z "$AT" ] && { echo "[seed/keycloak] admin token failed"; exit 1; }
  local H="Authorization: Bearer $AT"

  echo "[seed/keycloak] creating realm $REALM"
  curl -s -X POST "$BASE/admin/realms" -H "$H" -H "Content-Type: application/json" \
    -d "{\"realm\":\"$REALM\",\"enabled\":true}" >/dev/null || true

  echo "[seed/keycloak] creating confidential client"
  CLIENT_ID="bench-client"
  curl -s -X POST "$BASE/admin/realms/$REALM/clients" -H "$H" -H "Content-Type: application/json" \
    -d "{\"clientId\":\"$CLIENT_ID\",\"enabled\":true,\"publicClient\":false,\"serviceAccountsEnabled\":true,\"directAccessGrantsEnabled\":true,\"standardFlowEnabled\":true,\"redirectUris\":[\"http://localhost/cb\"]}" >/dev/null || true
  local CID
  CID=$(curl -sf "$BASE/admin/realms/$REALM/clients?clientId=$CLIENT_ID" -H "$H" \
    | sed -n 's/.*"id":"\([^"]*\)".*/\1/p' | head -1)
  CLIENT_SECRET=$(curl -sf "$BASE/admin/realms/$REALM/clients/$CID/client-secret" -H "$H" \
    | sed -n 's/.*"value":"\([^"]*\)".*/\1/p')

  echo "[seed/keycloak] creating benchmark user"
  curl -s -X POST "$BASE/admin/realms/$REALM/users" -H "$H" -H "Content-Type: application/json" \
    -d "{\"username\":\"$BENCH_USERNAME\",\"enabled\":true,\"email\":\"bench@bench.dev\",\"emailVerified\":true,\"credentials\":[{\"type\":\"password\",\"value\":\"$BENCH_PASSWORD\",\"temporary\":false}]}" >/dev/null || true
  write_seed
}

# ---------------------------------------------------------------------------
seed_zitadel() {
  # Zitadel has no scriptable password grant, so provisioning goes through its
  # management API authenticated with the admin PAT that the compose mints on
  # first-init (ZITADEL_FIRSTINSTANCE_PATPATH -> /machinekey/pat.txt). We read
  # that PAT and create:
  #   * a machine (service) user + generated secret  -> the client_credentials
  #     client the token/refresh scenarios use (BENCH_CLIENT_ID/SECRET);
  #   * a project + an API application (BASIC auth)   -> the resource-server the
  #     introspection scenario authenticates as (BENCH_INTROSPECT_CLIENT_ID/SECRET),
  #     because Zitadel introspects with an API app's creds, NOT the machine
  #     user's. The machine-user token is minted with the project in its audience
  #     (see the adapter) so introspection reports it active.
  # Manual override: export BENCH_CLIENT_ID/BENCH_CLIENT_SECRET to skip the API.
  REALM="zitadel"
  local container="${BENCH_ZITADEL_CONTAINER:-bench-zitadel}"

  if [ -n "${BENCH_CLIENT_ID:-}" ] && [ -n "${BENCH_CLIENT_SECRET:-}" ]; then
    echo "[seed/zitadel] using pre-supplied BENCH_CLIENT_ID/SECRET (manual mode)"
    CLIENT_ID="$BENCH_CLIENT_ID"; CLIENT_SECRET="$BENCH_CLIENT_SECRET"
    PROJECT_ID="${BENCH_PROJECT_ID:-}"
    INTROSPECT_CLIENT_ID="${BENCH_INTROSPECT_CLIENT_ID:-}"
    INTROSPECT_CLIENT_SECRET="${BENCH_INTROSPECT_CLIENT_SECRET:-}"
    write_seed; return
  fi

  # Read the first-init PAT out of the container (docker cp works on the
  # distroless image, which has no shell for `docker exec cat`).
  local patfile; patfile=$(mktemp)
  if ! docker cp "${container}:/machinekey/pat.txt" "$patfile" 2>/dev/null || ! [ -s "$patfile" ]; then
    rm -f "$patfile"
    echo "[seed/zitadel] could not read the admin PAT from ${container}:/machinekey/pat.txt." >&2
    echo "               The FIRSTINSTANCE PAT is only minted on a FRESH init, so a DB" >&2
    echo "               volume created before this feature won't have one. Re-provision:" >&2
    echo "                   just target=zitadel bench-down    # wipes the volume (-v)" >&2
    echo "                   just target=zitadel profile=${BENCH_PROFILE:-p0-plaintext} bench-up" >&2
    echo "                   just target=zitadel bench-seed" >&2
    if [ "${BENCH_ZITADEL_ALLOW_UNSEEDED:-0}" != "1" ]; then exit 1; fi
    echo "[seed/zitadel] BENCH_ZITADEL_ALLOW_UNSEEDED=1 — writing jwks-only seed." >&2
    CLIENT_ID=""; CLIENT_SECRET=""; write_seed; return
  fi
  local PAT; PAT=$(cat "$patfile"); rm -f "$patfile"

  # The management API is gRPC-backed and proxied through the REST gateway; it
  # accepts requests slightly LATER than the OIDC endpoints that /debug/ready
  # (the bench-up gate) tracks — a create fired immediately 503s with
  # "dial tcp [::1]:8080: connection refused". Pre-flight on an authenticated
  # read until the API is actually live before provisioning.
  echo "[seed/zitadel] waiting for the management API to accept requests"
  local api_deadline=$(( $(date +%s) + 90 ))
  # -k: TLS profiles serve the throwaway private-CA cert (BASE may be https).
  until [ "$(curl -sk -o /dev/null -w '%{http_code}' -H "Authorization: Bearer $PAT" "$BASE/auth/v1/users/me")" = "200" ]; do
    if [ "$(date +%s)" -ge "$api_deadline" ]; then
      echo "[seed/zitadel] TIMEOUT: management API did not become ready at $BASE" >&2
      exit 1
    fi
    sleep 2
  done

  # Management-API helper: JSON call with the PAT; fail closed on any non-2xx.
  zapi() {
    local method="$1" path="$2" body="${3:-}" resp code
    resp=$(curl -sSk -H "Authorization: Bearer $PAT" -H "Content-Type: application/json" \
      -X "$method" "$BASE$path" ${body:+--data "$body"} -w $'\n%{http_code}') || return 1
    code="${resp##*$'\n'}"; resp="${resp%$'\n'*}"
    if [ "$code" -lt 200 ] || [ "$code" -ge 300 ]; then
      echo "[seed/zitadel] $method $path -> HTTP $code: $resp" >&2; return 1
    fi
    printf '%s' "$resp"
  }

  echo "[seed/zitadel] creating machine (service) user for client_credentials"
  local mu
  mu=$(zapi POST /management/v1/users/machine \
    '{"userName":"bench-client","name":"bench-client","description":"AXIAM benchmark client_credentials user","accessTokenType":"ACCESS_TOKEN_TYPE_BEARER"}') || exit 1
  local mu_id; mu_id=$(echo "$mu" | jq -r '.userId')

  echo "[seed/zitadel] generating machine-user secret"
  local sec
  sec=$(zapi PUT "/management/v1/users/$mu_id/secret" '{}') || exit 1
  CLIENT_ID=$(echo "$sec" | jq -r '.clientId')
  CLIENT_SECRET=$(echo "$sec" | jq -r '.clientSecret')

  echo "[seed/zitadel] creating project + API application (introspection client)"
  local proj
  proj=$(zapi POST /management/v1/projects '{"name":"bench-project"}') || exit 1
  PROJECT_ID=$(echo "$proj" | jq -r '.id')
  local apiapp
  apiapp=$(zapi POST "/management/v1/projects/$PROJECT_ID/apps/api" \
    '{"name":"bench-introspect","authMethodType":"API_AUTH_METHOD_TYPE_BASIC"}') || exit 1
  INTROSPECT_CLIENT_ID=$(echo "$apiapp" | jq -r '.clientId')
  INTROSPECT_CLIENT_SECRET=$(echo "$apiapp" | jq -r '.clientSecret')

  [ -n "$CLIENT_ID" ] && [ -n "$CLIENT_SECRET" ] && [ -n "$PROJECT_ID" ] \
    && [ -n "$INTROSPECT_CLIENT_ID" ] && [ -n "$INTROSPECT_CLIENT_SECRET" ] \
    || { echo "[seed/zitadel] provisioning returned an empty id/secret" >&2; exit 1; }
  echo "[seed/zitadel] provisioned client_id=$CLIENT_ID project=$PROJECT_ID"
  write_seed
}

case "$TARGET" in
  axiam)    seed_axiam ;;
  keycloak) seed_keycloak ;;
  zitadel)  seed_zitadel ;;
  *) echo "unknown target: $TARGET" >&2; exit 1 ;;
esac
