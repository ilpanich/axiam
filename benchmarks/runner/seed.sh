#!/usr/bin/env bash
# seed.sh — provision a benchmark fixture (org/tenant/realm, a user, and a
# confidential client able to do client_credentials + refresh) for one target,
# then write a seed env file the runner sources:
#
#   results/<target>.seed.env   (BENCH_ORG_ID, BENCH_TENANT_ID, BENCH_CLIENT_ID,
#                                BENCH_CLIENT_SECRET, BENCH_USERNAME, BENCH_PASSWORD)
#
# Usage: seed.sh <target> [base_url]
set -euo pipefail

TARGET="${1:?target (axiam|keycloak|zitadel)}"
HERE="$(cd "$(dirname "$0")" && pwd)"
RESULTS="${BENCH_RESULTS_DIR:-$HERE/../results}"
mkdir -p "$RESULTS"
SEED_ENV="$RESULTS/${TARGET}.seed.env"

BENCH_USERNAME="${BENCH_USERNAME:-benchuser}"
BENCH_PASSWORD="${BENCH_PASSWORD:-Bench@User123!}"

# Seeding always talks plaintext to the app port; TLS is a transport concern
# exercised during the measured run, not during provisioning.
BASE="${2:-http://localhost:${BENCH_APP_PORT:-8090}}"

write_seed() {
  cat > "$SEED_ENV" <<EOF
# Generated by seed.sh for target=$TARGET
export BENCH_TARGET=$TARGET
export BENCH_ORG_ID=${ORG_ID:-}
export BENCH_TENANT_ID=${TENANT_ID:-}
export BENCH_TENANT_SLUG=${TENANT_SLUG:-default}
export BENCH_REALM=${REALM:-bench}
export BENCH_USERNAME=$BENCH_USERNAME
export BENCH_PASSWORD='$BENCH_PASSWORD'
export BENCH_CLIENT_ID=${CLIENT_ID:-bench-client}
export BENCH_CLIENT_SECRET='${CLIENT_SECRET:-}'
EOF
  echo "[seed] wrote $SEED_ENV"
}

# ---------------------------------------------------------------------------
seed_axiam() {
  local SURREAL="${SURREAL_URL:-http://localhost:8000}"
  local DB="${AXIAM__DB__DATABASE:-main}"
  ORG_ID=$(cat /proc/sys/kernel/random/uuid)
  TENANT_ID=$(cat /proc/sys/kernel/random/uuid)
  TENANT_SLUG=default

  echo "[seed/axiam] creating org+tenant via SurrealDB HTTP"
  resp=$(curl -sf -X POST "$SURREAL/sql" -H "Accept: application/json" \
    -H "Content-Type: text/plain" -H "surreal-ns: axiam" -H "surreal-db: $DB" \
    -u "${AXIAM__DB__USERNAME:-root}:${AXIAM__DB__PASSWORD:-root}" \
    --data-binary "
CREATE type::record('organization', '$ORG_ID') SET name='Bench Org', slug='bench-org', metadata={}, created_at=time::now(), updated_at=time::now();
CREATE type::record('tenant', '$TENANT_ID') SET organization_id='$ORG_ID', name='Bench Tenant', slug='$TENANT_SLUG', metadata={}, created_at=time::now(), updated_at=time::now();")
  echo "$resp" | grep -q '"status":"ERR"' && { echo "[seed/axiam] surreal seed failed: $resp"; exit 1; }

  echo "[seed/axiam] bootstrapping admin"
  curl -sf -X POST "$BASE/api/v1/admin/bootstrap" -H "Content-Type: application/json" \
    -d "{\"org_id\":\"$ORG_ID\",\"tenant_id\":\"$TENANT_ID\",\"email\":\"admin@bench.dev\",\"username\":\"admin\",\"password\":\"Bench@Admin123!\"}" \
    >/dev/null || true

  echo "[seed/axiam] logging in as admin"
  TOKEN=$(curl -sf -X POST "$BASE/api/v1/auth/login" -H "Content-Type: application/json" \
    -d "{\"tenant_id\":\"$TENANT_ID\",\"username_or_email\":\"admin\",\"password\":\"Bench@Admin123!\"}" \
    | sed -n 's/.*"access_token":"\([^"]*\)".*/\1/p')
  [ -z "$TOKEN" ] && { echo "[seed/axiam] admin login failed"; exit 1; }

  echo "[seed/axiam] creating benchmark user"
  curl -s -X POST "$BASE/api/v1/users" -H "Authorization: Bearer $TOKEN" \
    -H "Content-Type: application/json" \
    -d "{\"username\":\"$BENCH_USERNAME\",\"email\":\"bench@bench.dev\",\"password\":\"$BENCH_PASSWORD\"}" >/dev/null || true

  echo "[seed/axiam] creating confidential oauth2 client (client_credentials+refresh)"
  CLIENT_RESP=$(curl -s -X POST "$BASE/api/v1/oauth2-clients" -H "Authorization: Bearer $TOKEN" \
    -H "Content-Type: application/json" \
    -d '{"name":"bench-client","redirect_uris":["http://localhost/cb"],"grant_types":["client_credentials","refresh_token","authorization_code"],"scopes":["openid"]}')
  CLIENT_ID=$(echo "$CLIENT_RESP" | sed -n 's/.*"client_id":"\([^"]*\)".*/\1/p')
  CLIENT_SECRET=$(echo "$CLIENT_RESP" | sed -n 's/.*"client_secret":"\([^"]*\)".*/\1/p')
  [ -z "$CLIENT_ID" ] && echo "[seed/axiam] WARN: client creation response: $CLIENT_RESP"
  write_seed
}

# ---------------------------------------------------------------------------
seed_keycloak() {
  REALM="${REALM:-bench}"
  local ADMIN="${KC_ADMIN:-admin}" PW="${KC_ADMIN_PASSWORD:-admin}"
  echo "[seed/keycloak] obtaining admin token"
  local AT
  AT=$(curl -sf -X POST "$BASE/realms/master/protocol/openid-connect/token" \
    -d "grant_type=password&client_id=admin-cli&username=$ADMIN&password=$PW" \
    | sed -n 's/.*"access_token":"\([^"]*\)".*/\1/p')
  [ -z "$AT" ] && { echo "[seed/keycloak] admin token failed"; exit 1; }
  local H="Authorization: Bearer $AT"

  echo "[seed/keycloak] creating realm $REALM"
  curl -s -X POST "$BASE/admin/realms" -H "$H" -H "Content-Type: application/json" \
    -d "{\"realm\":\"$REALM\",\"enabled\":true}" >/dev/null || true

  echo "[seed/keycloak] creating confidential client"
  CLIENT_ID="bench-client"
  curl -s -X POST "$BASE/admin/realms/$REALM/clients" -H "$H" -H "Content-Type: application/json" \
    -d "{\"clientId\":\"$CLIENT_ID\",\"enabled\":true,\"publicClient\":false,\"serviceAccountsEnabled\":true,\"directAccessGrantsEnabled\":true,\"standardFlowEnabled\":true,\"redirectUris\":[\"http://localhost/cb\"]}" >/dev/null || true
  local CID
  CID=$(curl -sf "$BASE/admin/realms/$REALM/clients?clientId=$CLIENT_ID" -H "$H" \
    | sed -n 's/.*"id":"\([^"]*\)".*/\1/p' | head -1)
  CLIENT_SECRET=$(curl -sf "$BASE/admin/realms/$REALM/clients/$CID/client-secret" -H "$H" \
    | sed -n 's/.*"value":"\([^"]*\)".*/\1/p')

  echo "[seed/keycloak] creating benchmark user"
  curl -s -X POST "$BASE/admin/realms/$REALM/users" -H "$H" -H "Content-Type: application/json" \
    -d "{\"username\":\"$BENCH_USERNAME\",\"enabled\":true,\"email\":\"bench@bench.dev\",\"emailVerified\":true,\"credentials\":[{\"type\":\"password\",\"value\":\"$BENCH_PASSWORD\",\"temporary\":false}]}" >/dev/null || true
  write_seed
}

# ---------------------------------------------------------------------------
seed_zitadel() {
  # Zitadel provisioning is done through its management API with a service-user
  # PAT, which requires an initial console/login step that cannot be fully
  # scripted without a bootstrapped machine key. Provide the values manually:
  echo "[seed/zitadel] Zitadel requires a machine key / PAT to provision via the"
  echo "               management API. Set BENCH_CLIENT_ID / BENCH_CLIENT_SECRET"
  echo "               from a created service user, then re-run with those exported."
  REALM="zitadel"
  CLIENT_ID="${BENCH_CLIENT_ID:-}"
  CLIENT_SECRET="${BENCH_CLIENT_SECRET:-}"
  write_seed
}

case "$TARGET" in
  axiam)    seed_axiam ;;
  keycloak) seed_keycloak ;;
  zitadel)  seed_zitadel ;;
  *) echo "unknown target: $TARGET" >&2; exit 1 ;;
esac
